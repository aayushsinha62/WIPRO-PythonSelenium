#creating of arrays

#1D 2D and 3D

import numpy as np

#1D array
a=np.array([1,2,3,4])
print(a)

#2D array
a=np.array([[1,2],[2,3]])
print(a)

#3D array
a=np.array([[1,2],[3,4],[4,5]])
print(a)

#create array with specific data type
a=np.array([1,2,3],dtype=complex)
print(a)

#create array with specific data type
a=np.array([1,2,3],dtype=float)
print(a)

#create array with specific data type
a=np.array([1,2,3],dtype=int)
print(a)





